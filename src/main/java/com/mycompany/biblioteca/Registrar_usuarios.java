    private void boton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_guardarActionPerformed
        Object selectedItem = box_tipo_usuario.getSelectedItem(); // Obtener la opci√≥n seleccionada del combo box

        if (selectedItem != null && selectedItem.equals("Empleado")) {
            try {
                // Obtener datos del formulario para Empleado
                int id = Integer.parseInt(label_id1.getText()); // ID generado autom√°ticamente
                String nombre = field4.getText();
                String puesto = field2.getText();
                String horario = field1.getText();

                // Crear objeto Empleado
                Empleado empleado = new Empleado(id, nombre, puesto, horario);

                // Guardar en el archivo "empleados.txt"
                try (FileWriter fw = new FileWriter("C:\\Users\\Ssaid\\Documents\\NetBeansProjects\\Biblioteca\\src\\main\\java\\datos_guardados\\empleados.txt", true); BufferedWriter bw = new BufferedWriter(fw); PrintWriter out = new PrintWriter(bw)) {

                    // Guardar en el formato deseado
                    out.println(String.format("%d,%s,%s,%s",
                            empleado.getId(),
                            empleado.getNombre(),
                            empleado.getPuesto(),
                            empleado.getHorario()));

                    JOptionPane.showMessageDialog(this, "Empleado guardado correctamente.");
                }

                // Limpiar campos despu√©s de guardar
                label_id1.setText(""); // Limpiar el ID
                field4.setText(""); // Limpiar nombre
                field2.setText(""); // Limpiar puesto
                field1.setText(""); // Limpiar horario

            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al guardar el empleado.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else if (selectedItem != null && selectedItem.equals("Lector")) {
            try {
                // Obtener datos del formulario para Lector
                int id = Integer.parseInt(label_id1.getText()); // ID generado autom√°ticamente
                String nombre = field4.getText();
                String preferencias = field2.getText();
                String horasLectura = field1.getText();
                String vigenciaCredencial = field3.getText();

                // Crear objeto Lector
                Lector lector = new Lector(id, nombre, "", preferencias, horasLectura, vigenciaCredencial);

                // Guardar en el archivo "lectores.txt"
                try (FileWriter fw = new FileWriter("C:\\Users\\Ssaid\\Documents\\NetBeansProjects\\Biblioteca\\src\\main\\java\\datos_guardados\\lectores.txt", true); BufferedWriter bw = new BufferedWriter(fw); PrintWriter out = new PrintWriter(bw)) {

                    // Guardar en el formato deseado
                    out.println(String.format("%d,%s,%s,%s,%s",
                            lector.getId(),
                            lector.getNombre(),
                            lector.getPreferencias(),
                            lector.getHorasLectura(),
                            lector.getVigenciaCredencial()));

                    JOptionPane.showMessageDialog(this, "Lector guardado correctamente.");
                }

                // Limpiar campos despu√©s de guardar
                label_id1.setText(""); // Limpiar el ID
                field4.setText(""); // Limpiar nombre
                field2.setText(""); // Limpiar preferencias
                field1.setText(""); // Limpiar horas de lectura
                field3.setText(""); // Limpiar vigencia de credencial

            } catch (IOException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al guardar el lector.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una opci√≥n v√°lida para guardar.");
        }
    }//GEN-LAST:event_boton_guardarActionPerformed

    private void box_tipo_usuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_box_tipo_usuarioActionPerformed
        Object selectedItem = box_tipo_usuario.getSelectedItem(); // Obtener el elemento seleccionado

        if (selectedItem != null && selectedItem.equals("Empleado")) {
            label1.setText("Puesto");
            label.setText("Horario");
            field3.setVisible(false);
            label2.setVisible(false);
            label_id1.setText("");
        } else {
            label1.setText("Preferencias");
            label.setText("Horas de lectura");
            label2.setText("Vigencia de credencial");
            field3.setVisible(true);
            label2.setVisible(true);
            label_id1.setText("");
        }
    }//GEN-LAST:event_box_tipo_usuarioActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
// Dentro del evento del bot√≥n de generar ID
        int nuevoId;
        boolean idExiste;

        do {
            nuevoId = 10000 + new SecureRandom().nextInt(90000); // Genera ID de 5 d√≠gitos
            idExiste = false;

            try (BufferedReader br = new BufferedReader(new FileReader("libros.txt"))) {
                String linea;
                while ((linea = br.readLine()) != null) {
                    String idStr = linea.split(",")[0];
                    if (Integer.parseInt(idStr) == nuevoId) {
                        idExiste = true;
                        break;
                    }
                }
            } catch (IOException e) {
                // Si hay error al leer el archivo, asume que no existe el ID
                idExiste = false;
            }
        } while (idExiste);

        label_id1.setText(String.valueOf(nuevoId));

    }//GEN-LAST:event_jButton2ActionPerformed

